#!/usr/bin/env node
import { readFileSync, writeFileSync, existsSync, mkdirSync } from 'node:fs';
import { dirname } from 'node:path';

const SOURCE = process.env.SMOKE_MATRIX_ARTIFACT ?? 'artifacts/smoke-matrix.json';
const TARGET = process.env.GAME_GATES_TARGET ?? 'src/lib/generated/gameGates.ts';

const BETA_GAMES = [
  'whot',
  'trivia',
  'connect-4',
  'ettt',
  'logo',
  'landlord',
  'color-wahala',
  'hustle',
  'word-wahala',
];

if (!existsSync(SOURCE)) {
  console.error(`[gate-sync] missing smoke artifact: ${SOURCE}`);
  process.exit(1);
}

const artifact = JSON.parse(readFileSync(SOURCE, 'utf8'));
if (artifact.ok !== true) {
  console.error(`[gate-sync] smoke artifact is not green (ok=true required): ${SOURCE}`);
  process.exit(1);
}
const passed = new Set(
  (artifact.results ?? [])
    .filter((r) => r.status === 'pass')
    .map((r) => r.gameType),
);
const enabled = BETA_GAMES.filter((g) => passed.has(g));

const body = `// AUTO-GENERATED by scripts/sync-game-gates-from-smoke.mjs
// Source: ${SOURCE}
import type { GameType } from '@/lib/transport/types';

export const SMOKE_TESTED_BETA_GAMES = new Set<GameType>([
${enabled.map((g) => `  '${g}',`).join('\n')}
]);
`;

mkdirSync(dirname(TARGET), { recursive: true });
writeFileSync(TARGET, body, 'utf8');
console.log(`[gate-sync] wrote ${TARGET} (${enabled.length} enabled beta games)`);
